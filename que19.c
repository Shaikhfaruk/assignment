// WAP to print multi times Hello World using While Loop

#include <stdio.h>

int main()
{
    int a = 1;
    while (a <= 10)
    {
        printf("\n Hello World");
        a++;
    }

    return 0;
}

// Output

//  Hello World
//  Hello World
//  Hello World
//  Hello World
//  Hello World
//  Hello World
//  Hello World
//  Hello World
//  Hello World
//  Hello World