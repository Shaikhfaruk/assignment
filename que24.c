// WAP to print Even numbers 1 to 100

#include <stdio.h>

int main()
{
    int a = 0;
    printf("Even Numbers 1 to 100: %d", a);

    while (a <= 100)
    {
        if (a % 2 == 0)
        {
            printf("\n %d", a);
        }
        a++;
    }

    return 0;
}

